def non_empty_split(s): 
    return [ line.split() for line in s.splitlines() if line.strip() ]

def calc_radius(atype_fields):
    r = float(atype_fields[5]) * 0.5  # vdW sigma/2
    return r

radii_db = { fields[0]: calc_radius(fields) for fields in non_empty_split(atomtypes) }

def molecule_radii(s):
    return  [ radii_db[fields[1]] for fields in non_empty_split(s) ]

def write_rad(name, rlist):
    with open(name + '.rad', 'w') as f:
        f.write("#radii for {} generated by {}\n".format(name, sys.argv[0]))
        f.write("{}\n".format(len(rlist)))
        for x in rlist:
            f.write("{:.4g}\n".format(x))


